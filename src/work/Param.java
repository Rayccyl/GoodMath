package work;
public class Param {
    private int minX=Integer.MIN_VALUE;
    private int maxX=Integer.MAX_VALUE;
    private int minY=Integer.MIN_VALUE;
    private int maxY=Integer.MAX_VALUE;
    private int minZ=Integer.MIN_VALUE;
    private int maxZ=Integer.MAX_VALUE;



    public Param(int minX, int maxX, int minY, int maxY, int minZ, int maxZ) {
        this.minX = minX;
        this.maxX = maxX;
        this.minY = minY;
        this.maxY = maxY;
        this.minZ = minZ;
        this.maxZ = maxZ;
    }
    public Param(){}

    public int getMinX() {
        return minX;
    }

    public void setMinX(int minX) {
        this.minX = minX;
    }

    public int getMaxX() {
        return maxX;
    }

    public void setMaxX(int maxX) {
        this.maxX = maxX;
    }

    public int getMinY() {
        return minY;
    }

    public void setMinY(int minY) {
        this.minY = minY;
    }

    public int getMaxY() {
        return maxY;
    }

    public void setMaxY(int maxY) {
        this.maxY = maxY;
    }

    public int getMinZ() {
        return minZ;
    }

    public void setMinZ(int minZ) {
        this.minZ = minZ;
    }

    public int getMaxZ() {
        return maxZ;
    }

    public void setMaxZ(int maxZ) {
        this.maxZ = maxZ;
    }
    public boolean isInvalid(){
        return maxX<minX||maxY<minY||maxZ<minZ;
    } ;
}
